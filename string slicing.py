# slicing = crearea unui subșir prin extragerea elementelor dintr-un alt șir de caractere
# [strt:stop:step]

name = "Bro Code"

first_name = name[0:3]
last_name = name[4:8]
funky_name = name[0:8:2]
reversed_name = name[::-1]

print(first_name)
print(last_name)
print(funky_name)
print(reversed_name)