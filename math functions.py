import math

pi = 3

x = 1
y = 2
z = 3

print(round(pi)) # rotunjeste numarul
print(math.ceil(pi)) # rotunjeste numarul in sus
print(math.floor(pi)) # rotunjeste numarul in jos
print(pow(pi,2)) # face ridicarea la putere(e acelasi lucru cu **)
print(math.sqrt(pi)) # pune numarul sub radical
print(max(x,y,z)) # arata cel mai mare numar
print(min(x,y,z)) # arata cel mai mic numar

